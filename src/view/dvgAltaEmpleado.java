    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        ControladorBD conbd = new ControladorBD();
        int id = conbd.getnEmpleado() + 1;
        String nombre = this.txtNombre.getText();
        String apellidoP = this.txtApellidoPaterno.getText();
        String apellidoM = this.txtApellidoMaterno.getText();
        String usuario = this.txtUsuario.getText();
        int puesto;
        if(this.txtPuesto.getText().isEmpty() || this.txtPuesto.getText().isBlank() ){
            JOptionPane.showMessageDialog(null, "Rellene todos los campos por favor", "CAMPOS INCOMPLETOS", 2); //Advertencia
        } else{
            puesto = Integer.parseInt(txtPuesto.getText());            
            if(nombre.isBlank() || nombre.isEmpty() || apellidoP.isBlank() || apellidoP.isEmpty() || apellidoM.isBlank() || apellidoM.isEmpty() || usuario.isBlank() || usuario.isEmpty()){
            JOptionPane.showMessageDialog(null, "Rellene todos los campos por favor", "CAMPOS INCOMPLETOS", 2); //Advertencia
            }else{
                String contrasenia =  usuario + "_" +id;
                Empleado nuevoEmpleado = new Empleado(id, nombre, apellidoP, apellidoM, usuario, contrasenia, puesto);
                conbd.insertarEmpleado(nuevoEmpleado);
            }            
        }        
    }//GEN-LAST:event_btnAgregarActionPerformed

